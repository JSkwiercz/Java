package com.jskwiercz;

public class Bed {
    private int high;
    private int width;
    private int length;

    public Bed(int high, int width, int length) {
        this.high = high;
        this.width = width;
        this.length = length;
    }

    public int getHigh() {
        return high;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }
}
